use clap::{Arg, Command};

pub fn build_cli() -> Command {
    Command::new("rxev")
        .version("0.1.0")
        .about("Rust版xev事件测试器")
        .arg(Arg::new("display")
            .short('d')
            .long("display")
            .value_name("DISPLAY")
            .help("X server显示名")
            .takes_value(true))
        .arg(Arg::new("geometry")
            .short('g')
            .long("geometry")
            .value_name("GEOMETRY")
            .help("窗口大小和位置")
            .takes_value(true))
        .arg(Arg::new("bw")
            .long("bw")
            .value_name("PIXELS")
            .help("窗口边框宽度")
            .takes_value(true))
        .arg(Arg::new("bs")
            .long("bs")
            .value_name("TYPE")
            .help("backingstore属性")
            .takes_value(true))
        .arg(Arg::new("id")
            .long("id")
            .value_name("WINDOWID")
            .help("使用已有窗口ID")
            .takes_value(true))
        .arg(Arg::new("root")
            .long("root")
            .help("使用根窗口")
            .takes_value(false))
        .arg(Arg::new("s")
            .short('s')
            .help("设置save-under属性")
            .takes_value(false))
        .arg(Arg::new("name")
            .long("name")
            .value_name("NAME")
            .help("窗口标题")
            .takes_value(true))
        .arg(Arg::new("rv")
            .long("rv")
            .help("反色显示")
            .takes_value(false))
        .arg(Arg::new("event")
            .long("event")
            .value_name("EVENT_MASK")
            .help("指定事件掩码（可多次）")
            .multiple_occurrences(true)
            .takes_value(true))
        .arg(Arg::new("single_line")
            .short('1')
            .help("每事件一行显示")
            .takes_value(false))
        .arg(Arg::new("help")
            .long("help")
            .help("显示帮助信息")
            .takes_value(false))
        .arg(Arg::new("version")
            .long("version")
            .help("显示版本信息")
            .takes_value(false))
}